      <p>

La Dirección de Educación Superior es la dependencia de la Secretaría Académica de la UAEM, responsable de garantizar en coordinación con las Unidades Académicas el cumplimiento de los ejes estratégicos de formación e internacionalización del PIDE (2018-2023), y la primera dimensión del Modelo Universitario (formación), y con ello contribuir a la consolidación de la calidad de los Programas Educativos de Licenciatura.

</p>
    </div>
    <div class="tab-pane fade" id="v-pills-vision" role="tabpanel" aria-labelledby="v-pills-vision-tab" tabindex="0"> 
      <p>

Ser reconocida como un área estratégica de la Secretaría Académica, que guiará y asesorará a las Unidades Académicas en la implementación de los procesos educativos de forma innovadora, obteniendo un impacto favorable en la consolidación de los programas educativos de Licenciatura, logrando que la UAEM sea reconocida como una Universidad de calidad, en busca de la excelencia a nivel nacional e internacional.

</p>
    </div>
   <div class="tab-pane fade" id="v-pills-actividades" role="tabpanel" aria-labelledby="v-pills-actividades-tab" tabindex="0">

      <ul>
        <li>	Guiar y asesorar metodológicamente a las Unidades Académicas en los procesos de evaluación y acreditación, diseño y rediseño curricular de los programas educativos; en los procesos de “Tutorías” y “Seguimiento a las trayectorias académicas” y “Egresados”, así como la vinculación con las personas empleadoras y la regularización de las personas estudiantes que tienen incumplimientos en su trayectoria académica.</li>
<li>	Impulsar, promover y fortalecer el desarrollo profesional de docencia, a través del “Programa Institucional de Formación y Evaluación Docente”.
</li> 
          
          <li>Establecer comunicación y vinculación constante con los Comités Interinstitucionales para la Evaluación de la Educación Superior, el Consejo para la Acreditación de la Educación Superior y la Dirección General de Educación Superior, Consejo para la Evaluación de la Educación de Tipo Medio Superior, Coordinación Sectorial de Desarrollo Académico.</li>
              <li>Participar en las reuniones del comité de calidad de seguimiento a programas educativos de licenciatura, como parte de la incorporación de la UAEM, al Consorcio de Universidades Mexicanas (CUMex).</li>
              <li>Elaborar los avales de cancelación de programas educativos y dar seguimiento a su presentación en las sesiones del Consejo Universitario.</li>
              <li>Atender asuntos problemáticos y de urgencia relacionados con la operatividad de los programas educativos. Principalmente problemas relacionados con restricciones de operatividad del plan de estudios, casos diversos con el área de servicios escolares y normatividad, casos particulares con los programas vigentes en liquidación.</li>
              <li>Gestionar recursos institucionales para llevar a cabo las visitas de evaluación o acreditación de los organismos externos.</li>
        </ul>
